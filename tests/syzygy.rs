#[proptest]
fn probing_syzygy_tablebase_finds_best_move(
    #[strategy(select(SYZYGY_SUITE))] entry: (&'static str, &'static str, Wdl),
) {
    let (fen, m, wdl) = entry;
    let pos: Position = fen.parse()?;
    let moves = pos.moves().unpack().map(|m| (m, ())).collect();

    assert_eq!(
        SYZYGY
            .best(&pos, &moves)
            .and_then(|pv| Some((pv.score(), pv.head()?.to_string()))),
        if !m.is_empty() {
            Some((wdl.to_score(Ply::new(0)), m.to_string()))
        } else {
            None
        },
    );
}

#[proptest]
fn probing_syzygy_tablebase_finds_wdl(
    #[strategy(select(SYZYGY_SUITE))] entry: (&'static str, &'static str, Wdl),
) {
    let (fen, _, wdl) = entry;
    let pos = fen.parse()?;
    assert_eq!(SYZYGY.wdl(&pos), Some(wdl));
}
