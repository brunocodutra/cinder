#[proptest]
#[cfg_attr(miri, ignore)]
fn probing_syzygy_tablebase_finds_best_move(
    #[strategy(select(SYZYGY_SUITE))] entry: (&'static str, Wdl, i16, &'static str),
) {
    let (fen, wdl, _, m) = entry;
    let pos: Position = fen.parse()?;

    let moves = pos.moves().unpack().collect();
    let best = SYZYGY.as_ref().unwrap().best(&pos, &moves);

    assert_eq!(
        best.and_then(|pv| Some((pv.score(), pv.head()?.to_string()))),
        if !m.is_empty() {
            Some((wdl.to_score(zero()), m.to_string()))
        } else {
            None
        },
    );
}
